public class Profession {
	private int education;
	private int hours;
	private String jobOutlook;
	private String jobTitle;
	private double salary;
	private String upwardMobility;

	public Profession() {
	}

	public int getEducation() {
		return education;
		
	}

	public void setEducation(int education) {
		this.education = education;
	}

	public int getHours() {
		return hours;
	}

	public void setHours(int hours) {
		this.hours = hours;
	}

	public String getJobOutlook() {
		return jobOutlook;
	}

	public void setJobOutlook(String jobOutlook) {
		this.jobOutlook = jobOutlook;
	}

	public String getJobTitle() {
		return jobTitle;
	}

	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}

	public double getSalary() {
		return salary;
	}

	public void setSalary(double salary) {
		this.salary = salary;
	}

	public String getUpwardMobility() {
		return upwardMobility;
	}

	public void setUpwardMobility(String upwardMobility) {
		this.upwardMobility = upwardMobility;
	}

}
